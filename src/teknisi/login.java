    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        // TODO add your handling code here:
        String id_admin = null;
        String query = "SELECT * FROM admin WHERE username = ? AND password = ?"; //Query
        try{ //Eksepsi
            pst =  conn.prepareStatement(query); // Eksekusi Query
            pst.setString(1, txtusername.getText()); //Set Username
            pst.setString(2, txtpassword.getText()); //Set Password
            rs = pst.executeQuery(); //Pengambilan data
            while(rs.next()){ //Perulangan
                id_admin = rs.getString("id_admin"); // Mengambil data id_admin
            }
            rs.last(); //Penutupan deklarasi
            if(rs.getRow() == 1){ // Kondisi jika row = 1
               session.setId(id_admin); //setter id admin
               JOptionPane.showMessageDialog(null, "Selamat datang, "+txtusername.getText());
               home hm = new home();
               hm.setVisible(true);
               this.dispose();
               
            }else{
                JOptionPane.showMessageDialog(null, "Username dan Password salah!");
                txtusername.setText("");
                txtpassword.setText("");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Gagal pada database :"+e);
            System.out.println(e);
            txtusername.setText("");
            txtpassword.setText("");
            
        
        }
        
    }//GEN-LAST:event_btnloginActionPerformed

